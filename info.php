<h1>Furious Peter Griffin</h1>
<p>Furious Peter Griffin är ett studentprojekt, utvecklat av Ishaq Jound, i kursen JavaScript vid Blekinge tekniska högskola.
</p>
<p>Furious Peter Griffin spelet går ut på att fånga 25 kycklingar inom 30 sekunder utan att missa någon kyckling, missar man att fånga någon kyckling slutar spelet och spelet slutar också om man inte lyckas fånga 25 kycklingar inom 30 sekunder.</p>
<p>Kycklingen faller en efter en från himlen och det gäller att styra Peter så att han kan äta upp kycklingen innan den når golvet. Äter man upp kyckligen så dycker en ny fallande kyckling upp på en ny plats på spelplanen.</p>
<p>Ju fler kycklingar du fångar desto snabbare börjar kycklingen att falla.</p>
<p>Spelet styrs med piltangenterna.</p>
<hr>
